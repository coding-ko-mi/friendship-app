"""
FSM-состояния бота.

На MVP в боте остался ровно один интерактивный шаг — приём фото (остальная
анкета в Mini App). Состояние нужно, чтобы хендлер фото срабатывал только
во время регистрации, а не на любое присланное фото в любой момент.

Вынесено в отдельный модуль, чтобы и хендлер /start, и хендлер фото
импортировали одни и те же состояния без циклических импортов.
"""
from __future__ import annotations

from aiogram.fsm.state import State, StatesGroup


class Registration(StatesGroup):
    """Состояния процесса регистрации в боте."""

    # Ждём, пока пользователь пришлёт фото. После сохранения фото FSM
    # очищается: дальнейшая анкета уходит в Mini App, в боте делать нечего.
    waiting_for_photo = State()
