"""
Перечисления (Enum) для полей с фиксированным набором значений.

Используем Python Enum вместо «магических строк»: автодополнение в IDE,
защита от опечаток, единый источник правды для допустимых значений.
"""
import enum


class RequestType(enum.Enum):
    """Тип заявки на изменение состава компании."""

    JOIN = "join"      # одиночка просится в компанию
    INVITE = "invite"  # компания зовёт одиночку
    MERGE = "merge"    # компания сливается с компанией


class RequestStatus(enum.Enum):
    """Статус заявки на протяжении её жизненного цикла."""

    PENDING = "pending"    # создана, голосование ещё не началось
    VOTING = "voting"      # идёт голосование
    ACCEPTED = "accepted"  # принято (>= 75% голосов «за»)
    REJECTED = "rejected"  # отклонено


class AchievementCode(str, enum.Enum):
    """
    Коды достижений, которые ВЫДАЁТ наш код (модуль «Бэкенд: достижения»).

    Отличие от RequestType/RequestStatus: это НЕ тип колонки в БД. В таблице
    `achievements.code` лежит обычная строка. Этот Enum — контракт между
    местом, где происходит событие (создание компании, мэтч), и сервисом
    выдачи: чтобы выдавать по `AchievementCode.FOUNDER`, а не по строке
    "FOUNDER" с риском опечатки.

    Наследование от `str` означает, что `AchievementCode.FOUNDER == "FOUNDER"`
    и `.value` — это строка для БД. Поэтому код можно сравнивать со значением
    из таблицы напрямую.

    Здесь перечислены ТОЛЬКО достижения, у которых есть автоматическая выдача.
    Достижения без логики выдачи (визиты, благодарности и т.п.) живут лишь в
    справочнике (seed) и сюда не попадают, пока под них не появится фича.
    Добавить новое выдаваемое достижение = одна строка здесь + строка в seed +
    хук в том сервисе, где наступает событие.
    """

    FOUNDER = "FOUNDER"        # заложил начало компании (создание компании)
    FIRST_MEET = "FIRST_MEET"  # первое знакомство 1-на-1 (первый взаимный мэтч)
    FULL_HOUSE = "FULL_HOUSE"  # компания достигла 8 человек (выдаётся всем участникам)
    NO_BORDERS = "NO_BORDERS"  # в компании есть люди из 2+ городов
