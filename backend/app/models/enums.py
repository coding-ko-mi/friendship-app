"""
Перечисления (Enum) для полей с фиксированным набором значений.

Используем Python Enum вместо «магических строк»: автодополнение в IDE,
защита от опечаток, единый источник правды для допустимых значений.
"""
import enum


class RequestType(enum.Enum):
    """Тип заявки на изменение состава компании."""

    JOIN = "join"      # одиночка просится в компанию
    INVITE = "invite"  # компания зовёт одиночку
    MERGE = "merge"    # компания сливается с компанией


class RequestStatus(enum.Enum):
    """Статус заявки на протяжении её жизненного цикла."""

    PENDING = "pending"    # создана, голосование ещё не началось
    VOTING = "voting"      # идёт голосование
    ACCEPTED = "accepted"  # принято (>= 75% голосов «за»)
    REJECTED = "rejected"  # отклонено


class AchievementCode(enum.Enum):
    """
    Коды достижений — технические неизменяемые идентификаторы.

    По коду код выдаёт достижение (achievement_service.grant(code=...)) и
    seed наполняет справочник. Значение строки = поле achievements.code в БД.
    Эти строки НЕЛЬЗЯ менять после первого деплоя: по ним связаны выданные
    достижения. Имена/описания для пользователя — в seed-скрипте, не здесь.

    На MVP реально ВЫДАЮТСЯ: FOUNDER, FIRST_MEETING, NO_BORDERS, FULL_HOUSE.
    Остальные заведены в справочник для витрины («ещё не открыто»), но механики
    выдачи под них пока нет (нужны чек-ины, отметки людей, признание главным).
    """

    # --- Выдаются на MVP ---
    FOUNDER = "FOUNDER"              # заложил компанию (create_group)
    FIRST_MEETING = "FIRST_MEETING"  # первый взаимный мэтч (matching like)
    NO_BORDERS = "NO_BORDERS"        # в компании люди из 2+ городов
    FULL_HOUSE = "FULL_HOUSE"        # компания достигла порога участников

    # --- Заведены в справочник, выдача отложена (нет механики) ---
    COMMUNITY_LEADER = "COMMUNITY_LEADER"  # самая большая компания (переходящее)
    IRL = "IRL"                            # встреча всей компанией офлайн
    GRATITUDE = "GRATITUDE"                # отметка благодарности от человека
    SOUL = "SOUL"                          # единогласно признан главным звеном
